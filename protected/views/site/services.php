<script>
function populateFeatureData(){

    var html = '';
    $.getJSON("<?php echo Yii::app()->request->baseUrl; ?>/json/features-services.json", function (data) {
        $.each(data, function (index, value) {
            for (i=0; i < value.length; i++){
              
                
                html += '<div class="col-md-4 feature">';
                html += '<img src="<?php echo Yii::app()->request->baseUrl; ?>/images/'+ value[i].picture +'" class="img-features"><br><br>';
                html += '<span class="kind-featured">'+ value[i].title +'</span><br>';
                html += '<p class="featured-desc">'+ value[i].desc +'</p>';
                html += '</div>';
            };
            
            
        });
        $('#list-features').append(html);
    });
};

$(document).ready(function(){
    populateFeatureData();
});
                
</script>

